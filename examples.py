# List comprehensions: a way to process stuff in lists quickly

# Have this
ex1 = ["8", "10", "-5.5"]

# Want: [8, 10, 5.5]

####################################
# Have this
ex2 = ["hello", "WORLD", "pYthon"]

# Want: ["hello", "world", "python"]

####################################
# Have this
ex3 = [0, 5, -2, -4, 7]

# Want: [5, 7]

####################################
# Have this
ex4 = ["pvd", "boston", "wos", "newport"]

# Want: ["PVD", "WOS"]

####################################
# Have this
# Indices:  0, 1, 2, 3, 4, 5
ex5 =      [8, 3, 5, 9, 0, 2]

# Want: [0, 4, 5]  (indices of the even numbers)

